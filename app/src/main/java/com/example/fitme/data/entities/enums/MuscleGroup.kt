package com.example.fitme.data.entities.enums

enum class MuscleGroup {
    // CHEST
    UPPER_CHEST, // верх груди
    MIDDLE_CHEST, // середина груди
    LOWER_CHEST, // низ груди

    // BACK
    LATS, // широчайшие мышцы спины
    TRAPS, // трапециевидные мышцы
    LOWER_BACK, // поясница (разгибатели спины)

    // SHOULDERS
    FRONT_DELTS, // передняя дельта
    SIDE_DELTS, // средняя дельта
    REAR_DELTS, // задняя дельта

    // ARMS
    BICEPS, // бицепс
    BRACHIALIS, // брахиалис
    TRICEPS, // трицепс
    FOREARM, // предплечья

    // CORE
    ABS, // пресс
    OBLIQUES, // косые мышцы живота

    // GLUTES
    GLUTE_MAXIMUS, // большая ягодичная
    GLUTE_MEDIUS, // средняя ягодичная
    GLUTE_MINIMUS, // малая ягодичная

    // LEGS
    QUADS, // квадрицепсы
    HAMSTRINGS, // задняя поверхность бедра
    ADDUCTORS, // приводящие мышцы бедра

    // CALVES
    CALVES, // икроножные/камбаловидная (икры)

    // SERVICE
    CARDIO, // кардио
    FULL_BODY, // всё тело
    OTHER // другое
}
